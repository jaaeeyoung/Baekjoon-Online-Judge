'''
Algorithm
1. 로프를 k개 부터 1개까지 감소하면서 for문 돌기 : i
2. 로프의 중량을 증가시키면서 for문 돌기 : j
3. j가 i개 이상 있으면 가능 -> j * k : 들 수 있는 중량(w)
4. 매번 돌면서 max값 갱신하기
'''

# 입력
N = int(input())
weights = []
for i in range(N):
    weights.append(int(input()))

weights.sort()
max_weight = 0 # 최대 중량을 저장할 변수
for i in range(N, 0, -1): # 로프 i개 쓸 때
    for j in weights:
        if len(weights) - weights.index(j) >= i: # j 중량 이상의 로프가 i개 이상 있으면 들 수 있는 중량임(중량 j * 개수 i)
            if j * i > max_weight: # 중량의 최댓값을 갱신
                max_weight = j*i
        
    if max_weight > 0:
        break
                
print(max_weight)