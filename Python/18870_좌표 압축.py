import sys
sys.stdin.readline

# 입력
N = int(input())
data = list(map(int, input().split()))
temp = list(set(data))
temp.sort(reverse = True)

answer = {}
for i in range(len(temp)):
    answer[temp[i]] = len(temp) - i - 1

for i in data:
    print(answer[i], end = ' ')