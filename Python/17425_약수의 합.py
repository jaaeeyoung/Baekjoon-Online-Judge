'''
Algorithm
N까지의 약수 중 i라는 숫자는 N//i개 존재
-> i가 1부터 N까지 증가하면서 i의 개수 * i 더하기
'''
import sys
input = sys.stdin.readline

T = int(input())
for _ in range(T):
    N = int(input())
    sum_ = sum(range(N//2+1, N+1))
    for i in range(1, N//2+1):
        sum_ += i * (N//i)
    print(sum_)