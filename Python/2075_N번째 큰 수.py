'''
Algorithm
정렬
1. 입력받을 때마다 내림차순 정렬해 리스트에 N개 씩 저장
2. 입력을 모두 받았을 때는 리스트의 가장 마지막 값 출력
'''

import sys
input = sys.stdin.readline
N = int(input())
nums = []
for _ in range(N):
    nums += list(map(int, input().split()))
    nums.sort(reverse = True)
    nums = nums[:N]

print(nums[-1])