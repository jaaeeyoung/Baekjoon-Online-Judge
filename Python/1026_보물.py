'''
정렬
1. A 배열 오름차순 정렬
2. B 배열 내림차순 정렬
3. S = {A의 최솟값 * B의 최댓값}의 합
'''

N = int(input())
A = [int(num) for num in input().split()]
B = [int(num) for num in input().split()]

# A 배열 오름차순, B 배열 내림차순
A.sort()
B.sort(reverse=True)

# S = A최솟값 * B최댓값
S = 0
for idx in range(N):
    S += A[idx] * B[idx]

print(S)