'''
1. 각 좌표를 튜플로 받아 리스트에 저장
2. lambda 함수를 이용해 다중 조건으로 sort
    2.1. 1번 조건 : x[0]
    2.2. 2번 조건 : x[1]
'''

import sys
input = sys.stdin.readline

# 입력
N = int(input())
nums = []
for _ in range(N):
    nums.append(tuple(map(int, input().split())))

# 좌표 정렬
nums.sort(key = lambda x:(x[0], x[1]))
for i in nums:
    print(i[0], i[1])