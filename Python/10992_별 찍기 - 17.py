def func(num):

    if num == 1:
        return '*'

    return '*' + ' '*(2*(num-1)-1) + '*'
        
N = int(input())

if N > 1:
    for i in range(1, N):
        print(' '*(N-i) + func(i))
print('*'*(2*N-1))