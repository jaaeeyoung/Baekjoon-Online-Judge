'''
Algorithm
Dynamic Programming
점화식 : dp[n] = 2 * dp[n-1] + 1 (n = 짝수)
         dp[n] = 2 * dp[n-1] - 1 (n = 홀수)
'''

n = int(input())
dp = [0] * (n+1)
dp[1] = 1

for num in range(2, n+1):
    # num이 짝수일 때
    if num % 2 == 0:
        dp[num] = 2 * dp[num-1] + 1
    else: # 홀수일 때
        dp[num] = 2 * dp[num-1] - 1
print(dp[n]%10007)