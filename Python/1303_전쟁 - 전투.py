def dfs(x, y, count, team):
    # 지도 벗어나거나 이미 방문했으면 Return
    if x < 0 or x > M-1 or y < 0 or y > N-1 or visited[x][y] == True:
        return False
    
    # 이전 팀과 현재 팀이 동일하지 않으면 Return
    if team != graph[x][y]:
        return False
    
    # 현재 index 방문 처리
    visited[x][y] = True
    
    # 현재 덩어리의 칸 수 확인
    count[-1] += 1
    
    # 상하좌우 확인
    dfs(x-1, y, count, team)
    dfs(x+1, y, count, team)
    dfs(x, y-1, count, team)
    dfs(x, y+1, count, team)
    
    return count[-1]

