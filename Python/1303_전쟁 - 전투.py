# 가로, 세로
N, M = map(int, input().split())
graph = []
for _ in range(M):
    graph.append(list(input()))

visited = []
for i in range(M):
    temp = []
    for j in range(N):
        temp.append(False)
    visited.append(temp)

W_sum = 0
B_sum = 0
last = ''
count = []
for x in range(M):
    for y in range(N):

        # 덩어리 발견하면
        team = graph[x][y]
        count.append(0)
        answer = dfs(x, y, count, team)

        # 각 팀에 맞는 위력합 구하기
        if team == 'W':
            W_sum += answer * answer
        else:
            B_sum += answer * answer
print(W_sum, B_sum)