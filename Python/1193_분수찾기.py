x = int(input())

cum_sum = 1
i = 1
flag = 0
if x == 1:
    print('{}/{}'.format(1, 1))
else:
    while True:
        i += 1
        cum_sum += i # 1 + 2 + 3 + ... 누적합 하다가
        if cum_sum >= x: # 누적합이 x보다 커지면
            another = 1
            if i % 2 == 1: # 홀수
                flag = 1
            for j in range(cum_sum - x): # 누적합 - x 횟수만큼 되돌아가기
                    i -= 1
                    another += 1
            if flag:
                print('{}/{}'.format(another, i))
                break
            else:
                print('{}/{}'.format(i, another))
                break