X = int(input())

# 우향, 좌하향 대각선, 하향, 우상향 대각선
d = [(0, 1), (1, -1), (1, 0), (-1, 1)]

x, y = 0, 0
index = 0
for _ in range(2, X+1):
    x += d[index][0]
    y += d[index][1]
    if x == 0:
        if y % 2:
            index = 1
        else:
            index = 0
    elif y == 0:
        if x % 2:
            index = 2
        else:
            index = 3
        
print('{0}/{1}'.format(x+1, y+1))