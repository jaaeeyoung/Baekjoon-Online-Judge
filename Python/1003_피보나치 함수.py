'''
Dynamic Programming
점화식 : dp[i] = dp[i-1] + dp[i-2]
'''

T = int(input())

for _ in range(T):
    N = int(input())
    
    # dp Table 초기화
    dp = [[0, 0] for _ in range(N+1)]
    dp[0] = [1, 0]
    if N > 0:
        dp[1] = [0, 1]
    
    for idx in range(2, N+1):
        for i in range(2):
            dp[idx][i] = dp[idx-1][i] + dp[idx-2][i]
    
    print(dp[-1][0], dp[-1][1])