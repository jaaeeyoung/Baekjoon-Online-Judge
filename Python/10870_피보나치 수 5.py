def Fibo(n):
    if n == 0: # n이 0일 때 (첫 번째 피보나치 수) 0 Return
        return 0
    elif n == 1: # n이 1일 때 (두 번째 피보나치 수) 1 Return
        return 1
    else: # 그 외의 경우 index 1 전의 수와 Index 2 전의 수를 더한 값 Return
        return Fibo(n-1) + Fibo(n-2)
    
n = int(input())
print(Fibo(n))