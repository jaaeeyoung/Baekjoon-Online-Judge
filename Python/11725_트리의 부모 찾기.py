from collections import deque
import sys
input = sys.stdin.readline

def bfs(start):
    queue = deque([start])
    # 현재 노드 방문 처리
    visited[start] = True
    
    while queue:
        
        now = queue.popleft()
        
        # 인접 노드 중
        for i in graph[now]:
            if not visited[i]: # 방문하지 않은 노드가 있다면
                # 큐에 모두 추가하고 방문처리
                queue.append(i)
                visited[i] = True
                # 현재 노드가 인접한 노드의 부모 노드
                answer[i-2] = now
                
        if False not in visited:
            return

N = int(input())
graph = [[] for _ in range(N+1)]
visited = [False] * (N+1)
answer = [0 for _ in range(N-1)]
for _ in range(N-1):
    i, j = map(int, input().split())
    graph[i].append(j)
    if i not in graph[j]:
        graph[j].append(i)
        
bfs(1)
for i in answer:
    if i:
        print(i)