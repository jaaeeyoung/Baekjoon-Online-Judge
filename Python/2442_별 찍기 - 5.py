'''
Algorithm
1. 총 N번 FOR문을 돌아야 함
2. 왼쪽 빈칸 - N-1, N-2, ... 순으로 감소
3. 왼쪽 별 - 1, 2, 3, ... 순으로 증가
4. 오른쪽 별 - 0, 1, 2, ... 순으로 증가
'''

N = int(input())

for i in range(1, N+1): # 1 2 3
    # 빈칸
    for j in range(N-i, 0, -1): # 2 1
        print(' ', end = '')
    
    # 별
    for j in range(i):
        print('*', end = '')
    
    # 오른쪽 별
    for j in range(1, i):
        print('*', end = '')
    print('')