'''
Algorithm
1. 총 N번 FOR문을 돌아야 함
2. 왼쪽 빈칸 - N-1, N-2, ... 순으로 감소
3. 왼쪽 별 - 1, 2, 3, ... 순으로 증가
4. 오른쪽 별 - 0, 1, 2, ... 순으로 증가
'''

N = int(input())

for i in range(1, N+1): # 1 2 3
    print(' '*(N-i) + '*'*i + '*'*(i-1))