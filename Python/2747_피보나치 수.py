'''
Algorithm
1. 0일 때 0, 1일 때 1로 출력될 수 있도록 명시하고
2. 그 외의 상황에서 이전 값 + 이전 이전 값을 더해서 리스트에 추가
-> 리스트로 만들어서 현재 index-2 + 현재 index-1 의 값을 append
'''

n = int(input())

fibo = [0, 1]
for i in range(2, n+1):
    fibo.append(fibo[i-2] + fibo[i-1])
print(fibo[-1])