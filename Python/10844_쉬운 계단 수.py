'''
Algorithm
Dynamic Programming
점화식 : dp[n] = 2dp[n-1] - 1
'''

N = int(input())
dp = [0 for _ in range(N+1)]
dp[1] = 9
for num in range(2, N+1):
    dp[num] = 2*dp[num-1] - 1
print(dp[N]%1000000000)