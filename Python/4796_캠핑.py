'''
===================================================================================================================================
문제
===================================================================================================================================
[4796] 캠핑
===================================================================================================================================
시간 및 메모리 제한
===================================================================================================================================
# 시간 제한 : 1 초
# 메모리 제한 : 128 MB
===================================================================================================================================
문제 설명
===================================================================================================================================
# 등산가 김강산은 가족들과 함께 캠핑을 떠났다. 하지만, 캠핑장에는 다음과 같은 경고문이 쓰여 있었다.

# 캠핑장은 연속하는 20일 중 10일동안만 사용할 수 있습니다.

# 강산이는 이제 막 28일 휴가를 시작했다. 이번 휴가 기간 동안 강산이는 캠핑장을 며칠동안 사용할 수 있을까?

# 강산이는 조금 더 일반화해서 문제를 풀려고 한다. 

# 캠핑장을 연속하는 P일 중, L일동안만 사용할 수 있다. 강산이는 이제 막 V일짜리 휴가를 시작했다. 강산이가 캠핑장을 최대 며칠동안 사용할 수 
  있을까? (1 < L < P < V)
===================================================================================================================================
입력
===================================================================================================================================
# 입력은 여러 개의 테스트 케이스로 이루어져 있다. 각 테스트 케이스는 한 줄로 이루어져 있고, L, P, V를 순서대로 포함하고 있다. 모든 입력 
  정수는 int범위이다. 마지막 줄에는 0이 3개 주어진다.
===================================================================================================================================
출력
===================================================================================================================================
# 각 테스트 케이스에 대해서, 강산이가 캠핑장을 최대 며칠동안 사용할 수 있는지 예제 출력처럼 출력한다.
===================================================================================================================================
입출력 예
===================================================================================================================================
입력 1
5 8 20
5 8 17
0 0 0

출력 1
Case 1: 14
Case 2: 11
===================================================================================================================================
알고리즘 분류
===================================================================================================================================
# 수학
# 그리디 알고리즘
===================================================================================================================================
Log
===================================================================================================================================
' 2022-02-14-MON : 문제 파악
                   문제 해결
===================================================================================================================================
'''

usable_day = 0 # 누적 사용 가능한 날짜
remain_day = 0 # 남은 날짜
answer = []
while True:
    L, P, V = map(int, input().split())

    if L*P*V == 0: # 입력 값이 000이 아닌 경우에만 실행
        break

    usable_day = (V // P) * L # 휴가 동안 P일 중 L일 사용할 때의 날짜
    remain_day = V % P # 휴가 동안 P일 중 L일 사용하고 남는 날짜
    if remain_day > L: # 남은 날의 수가 L보다 크면
        usable_day += L # 최대로 사용할 수 있는 날은 L일
    else: # L보다 크지 않으면
        usable_day += remain_day # 남은 날 모두 사용 가능
    
    answer.append(usable_day)
    
for i in range(len(answer)):
    print('Case {}: {}'.format(i+1, answer[i]))