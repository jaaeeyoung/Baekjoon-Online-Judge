N = int(input())

i = 2
while i < N+1:
    if N % i == 0: # N이 i로 나눠지면
        print(i) # i 출력하고
        N = N // i # 몫으로 N값 갱신
    else: # 나누어지지 않는다면
        i += 1 # i값을 1 올림
        
    if N == 1: # 몫이 1이되면 break
        break