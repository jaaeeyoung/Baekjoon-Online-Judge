# 카운팅 정렬?
import sys
input = sys.stdin.readline

# 입력
N = int(input())
# 각 입력된 숫자의 등장 횟수를 셀 변수
count = [0]*10001
for i in range(N):
    # 입력되는 숫자의 Index에 1 추가
    count[int(input())] += 1
    
# 출력
# count에 저장된 각 숫자의 횟수만큼 해당 숫자(Index) 출력
for i in range(len(count)):
    for j in range(count[i]):
        print(i)