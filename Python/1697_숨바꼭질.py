from collections import deque
def bfs(start):
    
    # 현재 위치에 이미 방문한 적 있으면 무시
    if visited[start] > 0:
        return False
    
    # 현재 위치 방문 기록
    visited[start] = 1
    
    queue = deque([start])
    while queue:
        
        x = queue.popleft()

        # 위치 이동
        for i in graph[x]:
            # 이미 방문한 곳이라면 무시
            if i < 0 or i > MAX-1 or visited[i]>0:
                continue

            queue.append(i)
            visited[i] = visited[x] + 1
            
        if x == K:
            return visited[K] - 1
    return True

# 입력
N, K = map(int, input().split())
MAX = 100001
# 그래프 생성
graph = [[] for _ in range(MAX)]
for i in range(MAX):
    graph[i] = [i-1, i+1, i*2]
visited = [0] * (MAX)

print(bfs(N))
