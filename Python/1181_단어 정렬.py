# 입력
N = int(input())
words = []
for _ in range(N):
    words.append(input())

# 중복 제거
words = list(set(words))

# lambda를 이용해 길이 -> 사전순으로 정렬
words.sort(key = lambda x: (len(x), x))
for i in words:
    print(i)