'''
Algorithm
1. 카이사르: 원 문자 형태로 Dictionary 만들기
2. 입력을 받아 각 문자의 원 문자가 무엇인지 Dictionary에서 찾기
'''

Caesar = {
    'A':'X',
    'B':'Y',
    'C':'Z',
    'D':'A',
    'E':'B',
    'F':'C',
    'G':'D',
    'H':'E',
    'I':'F',
    'J':'G',
    'K':'H',
    'L':'I',
    'M':'J',
    'N':'K',
    'O':'L',
    'P':'M',
    'Q':'N',
    'R':'O',
    'S':'P',
    'T':'Q',
    'U':'R',
    'V':'S',
    'W':'T',
    'X':'U',
    'Y':'V',
    'Z':'W'
}

words = input()
for i in words:
    print(Caesar[i], end = '')
    
'''
Algorithm
1. 단어를 입력받아 각 문자를 리스트에 담음
2. 각 문자의 ASCII 코드 - 3의 글자 출력
-> 문자가 D 이전이면 Z의 ASCII 코드 - 2, -1, -0
'''

words = input()
for i in words:
    if ord(i) < 68:
        print(chr(ord(i)+23), end = '')
    else:
        print(chr(ord(i)-3), end = '')