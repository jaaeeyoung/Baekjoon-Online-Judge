'''
Algorithm
Dynamic Programming
점화식 : dp[n] = dp[n-2] + dp[n-1]
'''

n = int(input())
dp = [0] * (n+1)
dp[1] = 1
if n > 1:
    dp[2] = 2

for num in range(3, n+1):
    dp[num] = dp[num-1] + dp[num-2]
print(dp[n]%10007)