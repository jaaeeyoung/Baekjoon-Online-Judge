'''
Algorithm
완전 탐색 이용
1. T에서 가장 맨 뒤에 A가 있다면 A 빼기
2. T에서 가장 맨 뒤에 B가 있다면 B 빼고 reverse
3. 1, 2 과정 후 S와 동일한지 확인
4. T의 길이가 0이 되면 BREAK
'''

import sys
input = sys.stdin.readline

S = input().rstrip()
T = input().rstrip()

answer = '0'
while T:
    # T의 가장 맨 뒤에 A가 있다면 A 빼기
    if T[-1] == 'A':
        T = T[:-1]
    else: # T의 가장 맨 뒤에 B가 있다면 B 빼고 reverse
        T = T[:-1]
        T = T[::-1]
    
    # T의 값이 S와 같으면 break
    if T == S:
        answer = '1'
        break
print(answer)