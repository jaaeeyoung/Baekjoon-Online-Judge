def dfs(x, y, count):

    # x, y 범위가 그래프 밖으로 벗어나면 무시
    if x < 0 or x > N-1 or y < 0 or y > N-1:
        return False

    # 이미 방문한 기록이 있거나 0이면 무시
    if graph[x][y] > 1 or graph[x][y] == 0:
        return False
    
    # 현재 위치 방문 기록
    graph[x][y]  = 2
    count.append(0)

    # 상, 하, 좌, 우로 이동
    dfs(x-1, y, count)
    dfs(x+1, y, count)
    dfs(x, y-1, count)
    dfs(x, y+1, count)
    
    return True

N = int(input())
graph = [[] for _ in range(N)]
for i in range(N):
    graph[i] = list(map(int, input()))

answer = []
for x in range(N):
    for y in range(N):
        count = [] # 몇 칸인지 셀 리스트
        if dfs(x, y, count):
            answer.append(len(count))

print(len(answer))
answer.sort()
for i in answer:
    print(i)