'''
Algorithm
1. 방 번호를 리스트에 담아 입력 받음
2. Dictionary로 각 숫자의 빈도 수를 셈
-> 6과 9는 하나의 key 사용
3. 입력이 끝난 후 6과 9에 대한 value // 2 + 1 하기
4. 최빈값 출력
'''

N = input()
nums = {}
flag = 0 # 6이나 9가 포함되어있는 경우 1
for i in N:
    if i == '6' or i == '9':
        i = '6'
        flag = 1
    if i not in nums:
        nums[i] = 1
    else:
        nums[i] += 1

# 6과 9의 value는 2로 나눔
if flag == 1:
    if nums['6'] % 2 == 0:
        nums['6'] = nums['6'] // 2
    else:
        nums['6'] = nums['6'] // 2 + 1

print(max(nums.values()))