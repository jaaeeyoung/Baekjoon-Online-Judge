'''
===================================================================================================================================
문제
===================================================================================================================================
[2751] 수 정렬하기 2
===================================================================================================================================
시간 및 메모리 제한
===================================================================================================================================
# 시간 제한 : 2 초
# 메모리 제한 : 256 MB
===================================================================================================================================
문제 설명
===================================================================================================================================
# N개의 수가 주어졌을 때, 이를 오름차순으로 정렬하는 프로그램을 작성하시오.
===================================================================================================================================
입력
===================================================================================================================================
# 첫째 줄에 수의 개수 N(1 ≤ N ≤ 1,000,000)이 주어진다. 둘째 줄부터 N개의 줄에는 수가 주어진다. 이 수는 절댓값이 1,000,000보다 작거나 
  같은 정수이다. 수는 중복되지 않는다.
===================================================================================================================================
출력
===================================================================================================================================
# 첫째 줄부터 N개의 줄에 오름차순으로 정렬한 결과를 한 줄에 하나씩 출력한다.
===================================================================================================================================
입출력 예
===================================================================================================================================
입력
5
5
4
3
2
1

출력
1
2
3
4
5
===================================================================================================================================
알고리즘 분류
===================================================================================================================================
# 정렬
===================================================================================================================================
Log
===================================================================================================================================
' 2022-02-12-SAT : 문제 파악
                   시간 초과
                   입력 받는 방식을 sys.stdin.readline으로 변경
                   문제 해결
===================================================================================================================================
'''
import sys
input = sys.stdin.readline
# 입력
N = int(input())

nums = []
for i in range(N):
    nums.append(int(input()))
    
# nums 정렬
nums.sort()
# 출력
for i in nums:
    print(i)