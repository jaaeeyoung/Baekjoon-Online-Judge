def star(N, expo):
    # N/3 * N/3 정사각형
    for i in range(1, N+1):
        for j in range(1, N+1):
            print('*', end = '')
            if j%3 == 0:
                print('\n')
            elif i == i//2 + 1 and j == j // 2 + 1:
                print('^', end = '')
    # N/3의 패턴으로 둘러싼 형태
    
