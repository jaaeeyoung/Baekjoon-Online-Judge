'''
===================================================================================================================================
문제
===================================================================================================================================
[2292] 벌집
===================================================================================================================================
시간 및 메모리 제한
===================================================================================================================================
# 시간 제한 : 2 초
# 메모리 제한 : 128 MB
===================================================================================================================================
문제 설명
===================================================================================================================================
# 위의 그림과 같이 육각형으로 이루어진 벌집이 있다. 그림에서 보는 바와 같이 중앙의 방 1부터 시작해서 이웃하는 방에 돌아가면서 1씩 증가하
  는 번호를 주소로 매길 수 있다. 숫자 N이 주어졌을 때, 벌집의 중앙 1에서 N번 방까지 최소 개수의 방을 지나서 갈 때 몇 개의 방을 지나가는지
  (시작과 끝을 포함하여)를 계산하는 프로그램을 작성하시오. 예를 들면, 13까지는 3개, 58까지는 5개를 지난다.
===================================================================================================================================
입력
===================================================================================================================================
# 첫째 줄에 N(1 ≤ N ≤ 1,000,000,000)이 주어진다.
===================================================================================================================================
출력
===================================================================================================================================
# 입력으로 주어진 방까지 최소 개수의 방을 지나서 갈 때 몇 개의 방을 지나는지 출력한다.
===================================================================================================================================
입출력 예
===================================================================================================================================
입력 1
13

출력 1
3

입력 2
58

출력 2
5
===================================================================================================================================
알고리즘 분류
===================================================================================================================================
# 수학
===================================================================================================================================
Log
===================================================================================================================================
' 2022-02-12-SAT : 문제 파악
                   N값 까지 도착은 하는데 최단거리를 못찾음
                   최단거리 찾도록 고쳤지만 틀림
===================================================================================================================================
'''
# 입력
N = int(input())

# 누적합
cum_sum = 1
# 몇 번째 줄인지 체크할 변수
count = 1
# 입력값이 1이면 1반환
if N == 1:
    print(count)
else: # 입력값이 2이상이면
    while True: # 한 줄에 6의 배수씩 증가하면서 누적합과 입력값 비교
        cum_sum += 6 * count
        count += 1
        if cum_sum >= N: # 누적합이 입력값보다 크다면 입력값이 해당 줄(Count)에 있다는 의미
            print(count)
            break